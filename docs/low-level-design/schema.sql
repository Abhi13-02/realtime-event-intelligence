-- =============================================================
-- Low-Level Design: Database Schema
-- PostgreSQL 15+ with pgvector extension
-- =============================================================
-- Raw articles (pre-deduplication) are NOT stored in a separate database.
-- Kafka's 7-day retention on the raw-articles topic serves as the raw storage layer.
-- PostgreSQL only stores articles that have passed stage 1 (deduplication).
-- Alert checks compare the pipeline-mapped topic sensitivity threshold against
-- relevance_score only. credibility_score is stored for transparency
-- but does not affect alerting logic.

CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE EXTENSION IF NOT EXISTS vector;

-- -------------------------------------------------------------
-- 1. USERS
-- google_sub: Google's stable subject ID for the user — sourced
-- from the NextAuth JWT on every authenticated request. Used for
-- just-in-time account creation on first sign-in (no register
-- endpoint). password_hash and refresh_tokens are omitted —
-- Google is the identity provider; no passwords are stored.
-- -------------------------------------------------------------
CREATE TABLE users (
    id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    name             TEXT NOT NULL,
    email            TEXT NOT NULL UNIQUE,
    google_sub       TEXT UNIQUE,
    phone_number     TEXT,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_users_email ON users (email);

-- -------------------------------------------------------------
-- 2. SOURCES
-- Curated list of RSS feeds, Reddit, Hacker News.
-- Managed by admins — not user-configurable in v1.
-- -------------------------------------------------------------
CREATE TABLE sources (
    id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    name             TEXT NOT NULL,
    url              TEXT NOT NULL UNIQUE,
    type             TEXT NOT NULL,
    credibility_score FLOAT NOT NULL DEFAULT 0.5 CHECK (credibility_score BETWEEN 0 AND 1),
    poll_interval    INT NOT NULL DEFAULT 600,   -- seconds; default 10 mins
    is_active        BOOLEAN NOT NULL DEFAULT TRUE,
    last_crawled_at  TIMESTAMPTZ
);

-- -------------------------------------------------------------
-- 3. TOPICS
-- Each topic belongs to one user.
-- description: optional user-provided keywords or context.
-- expanded_description: Gemini-generated expansion of name +
-- description. This is what Sentence-BERT embeds, not the raw
-- name.
-- embedding: generated from expanded_description via
-- Sentence-BERT.
-- sensitivity: user-facing alert sensitivity level.
-- 'broad' -> 0.55 internally, 'balanced' -> 0.65, 'high' -> 0.75
-- mapping to float happens in the pipeline, not stored in DB.
-- -------------------------------------------------------------
CREATE TABLE topics (
    id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    user_id          UUID NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    name             TEXT NOT NULL,
    description      TEXT,
    expanded_description TEXT,
    embedding        VECTOR(768),               -- generated by Sentence-BERT (all-mpnet-base-v2)
    sensitivity      TEXT NOT NULL DEFAULT 'balanced'
                         CHECK (sensitivity IN ('broad', 'balanced', 'high')),
    is_active        BOOLEAN NOT NULL DEFAULT TRUE,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER topics_updated_at
    BEFORE UPDATE ON topics
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at();

CREATE INDEX idx_topics_user_id ON topics (user_id);

-- -------------------------------------------------------------
-- 4. TOPIC CHANNELS
-- One topic can have multiple delivery channels.
-- Separated from topics table to support multi-channel per topic.
-- -------------------------------------------------------------
CREATE TABLE topic_channels (
    id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    topic_id         UUID NOT NULL REFERENCES topics (id) ON DELETE CASCADE,
    channel          TEXT NOT NULL CHECK (channel IN ('email', 'sms', 'websocket')),
    UNIQUE (topic_id, channel)                  -- one row per channel per topic
);

CREATE INDEX idx_topic_channels_topic_id ON topic_channels (topic_id);

-- -------------------------------------------------------------
-- 5. ARTICLES
-- Rejected articles are not stored in PostgreSQL - they are
-- discarded at the pipeline stage. Only articles that pass
-- deduplication are written here.
--
-- pipeline_status values:
--   passed_dedup         -> survived stage 1 (deduplication)
--   processed            -> survived all stages, summary generated
--
-- embedding: Sentence-BERT vector for deduplication.
-- summary: generated once by Gemini, reused for all matching users.
-- -------------------------------------------------------------
CREATE TABLE articles (
    id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    source_id        UUID NOT NULL REFERENCES sources (id),
    url              TEXT NOT NULL UNIQUE,
    headline         TEXT NOT NULL,
    content          TEXT,
    summary          TEXT,                      -- NULL until stage 5 completes
    embedding        VECTOR(768),
    pipeline_status  TEXT NOT NULL DEFAULT 'passed_dedup'
                         CHECK (pipeline_status IN ('passed_dedup', 'processed')),
    published_at     TIMESTAMPTZ,
    crawled_at       TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_articles_url        ON articles (url);
CREATE INDEX idx_articles_source_id  ON articles (source_id);
CREATE INDEX idx_articles_crawled_at ON articles (crawled_at DESC);

-- pgvector IVFFlat index for fast ANN search during deduplication
CREATE INDEX idx_articles_embedding ON articles
    USING ivfflat (embedding vector_cosine_ops) WITH (lists = 100);

-- -------------------------------------------------------------
-- 6. ARTICLE TOPIC MATCHES
-- Junction table: many-to-many between articles and topics.
-- relevance_score : raw cosine similarity (Sentence-BERT)
-- credibility_score: copied from sources.credibility_score at match time
-- Alert checks compare the pipeline-mapped topic sensitivity threshold
-- against relevance_score.
-- -------------------------------------------------------------
CREATE TABLE article_topic_matches (
    id                UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    article_id        UUID NOT NULL REFERENCES articles (id) ON DELETE CASCADE,
    topic_id          UUID NOT NULL REFERENCES topics (id) ON DELETE CASCADE,
    relevance_score   FLOAT NOT NULL CHECK (relevance_score BETWEEN -1 AND 1),
    credibility_score FLOAT NOT NULL CHECK (credibility_score BETWEEN 0 AND 1),
    created_at        TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (article_id, topic_id)               -- one match row per pair
);

CREATE INDEX idx_atm_article_id ON article_topic_matches (article_id);
CREATE INDEX idx_atm_topic_id   ON article_topic_matches (topic_id);

-- relevance_score is also denormalized from article_topic_matches for the
-- same reason.
-- -------------------------------------------------------------
-- 7. ALERTS
-- Delivery history. One row per user per article per channel.
-- topic_id is denormalized here (also derivable via article_topic_matches)
-- to avoid joins on every dashboard read — intentional denormalization.
-- -------------------------------------------------------------
CREATE TABLE alerts (
    id               UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    user_id          UUID NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    article_id       UUID NOT NULL REFERENCES articles (id) ON DELETE CASCADE,
    topic_id         UUID NOT NULL REFERENCES topics (id) ON DELETE CASCADE,
    relevance_score  FLOAT NOT NULL CHECK (relevance_score BETWEEN -1 AND 1),
    channel          TEXT NOT NULL CHECK (channel IN ('email', 'sms', 'websocket')),
    status           TEXT NOT NULL DEFAULT 'pending'
                         CHECK (status IN ('pending', 'sent', 'failed')),
    sent_at          TIMESTAMPTZ,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    -- Prevents duplicate alert rows on Kafka message replay.
    -- If the pipeline crashes between publishing to matched-articles
    -- and committing the Kafka offset, the same message is replayed.
    -- This constraint ensures the second INSERT is silently ignored.
    UNIQUE (user_id, article_id, topic_id, channel)
);

-- Composite index on (user_id, created_at DESC) supports the primary
-- dashboard query: fetch all alerts for a user ordered by recency.
-- Replaces separate user_id and created_at indexes — PostgreSQL can
-- satisfy both the filter and the sort from a single index scan.
CREATE INDEX idx_alerts_user_id_created_at ON alerts (user_id, created_at DESC);
CREATE INDEX idx_alerts_status             ON alerts (status);

-- =============================================================
-- INTELLIGENCE LAYER TABLES
-- The following tables support the sub-theme discovery and
-- sentiment tracking features. They are entirely additive —
-- no existing tables above are modified.
--
-- Data sources in this layer:
--   GDELT  — news articles; drive clustering and alerts
--   Reddit — posts + comments; drive sentiment only
--
-- The analytics consumer and trend_snapshots table were
-- deliberately omitted. Sub-theme snapshots (table 12)
-- provide finer-grained per-sub-theme volume and sentiment
-- history, making a separate coarse topic-level volume table
-- redundant.
-- =============================================================

-- -------------------------------------------------------------
-- 8. REDDIT COMMENTS
-- Stored for Reddit articles only. Comments are used
-- exclusively for sentiment analysis inside the sub-theme
-- discovery job (see intelligence-lld.md).
-- They are NOT embedded, NOT used for topic matching, and NOT
-- surfaced to users as alerts. They are linked to the article
-- row that represents the Reddit post via article_id.
--
-- score: Reddit upvote score — used as a weight during VADER
-- sentiment aggregation. Higher-scored comments carry more
-- signal weight than low-scored ones.
--
-- The pipeline inserts these at Stage 3 in bulk, only when
-- source_id matches the Reddit source constant and the
-- incoming Kafka message contains a non-empty comments array.
-- -------------------------------------------------------------
CREATE TABLE reddit_comments (
    id          UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    article_id  UUID NOT NULL REFERENCES articles (id) ON DELETE CASCADE,
    body        TEXT NOT NULL,
    score       INT NOT NULL DEFAULT 0,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_reddit_comments_article_id ON reddit_comments (article_id);

-- -------------------------------------------------------------
-- 9. SUB THEMES
-- One row per discovered sub-theme within a topic.
-- Sub-themes are discovered automatically by the sub-theme
-- discovery Celery task, which runs every 6 hours.
-- No user input is required or accepted.
--
-- centroid: the mean embedding vector of all GDELT articles
-- that formed this cluster. Stored so Reddit posts can be
-- assigned to their nearest sub-theme via pgvector ANN search
-- without re-clustering from scratch on every run.
--
-- label + description: generated by LangChain + Cohere.
-- Called once when the sub-theme is new, and again only when
-- it has changed significantly (see SUBTHEME_RELABEL_VOLUME_
-- CHANGE_THRESHOLD in intelligence-lld.md). NULL until the
-- first labeling call completes.
--
-- keywords: top N terms extracted from cluster article
-- headlines — passed as input to the LLM labeling prompt so
-- the model has concrete signal beyond just the volume counts.
--
-- representative_article_id: the GDELT article whose
-- embedding is closest to the centroid — shown to users as
-- the canonical example of what this sub-theme is about.
-- ON DELETE SET NULL so the sub-theme row survives if the
-- article is removed.
--
-- status lifecycle:
--   emerging  → first seen this run, not yet established
--   active    → consistently present across multiple runs
--   declining → volume falling toward disappearing threshold
--   inactive  → volume dropped below disappearing threshold
-- Transitions are driven entirely by env var thresholds.
-- See intelligence-lld.md Section 5 for full state machine.
-- -------------------------------------------------------------
CREATE TABLE sub_themes (
    id                          UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    topic_id                    UUID NOT NULL REFERENCES topics (id) ON DELETE CASCADE,
    label                       TEXT,
    description                 TEXT,
    keywords                    TEXT[],
    centroid                    VECTOR(768),
    representative_article_id   UUID REFERENCES articles (id) ON DELETE SET NULL,
    first_seen_at               TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    last_seen_at                TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    label_generated_at          TIMESTAMPTZ,
    status                      TEXT NOT NULL DEFAULT 'emerging'
                                    CHECK (status IN ('emerging', 'active', 'declining', 'inactive')),
    created_at                  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at                  TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TRIGGER sub_themes_updated_at
    BEFORE UPDATE ON sub_themes
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at();

CREATE INDEX idx_sub_themes_topic_id ON sub_themes (topic_id);
CREATE INDEX idx_sub_themes_status   ON sub_themes (status);

-- pgvector IVFFlat index for centroid similarity lookups.
-- Used when assigning Reddit posts to their nearest sub-theme
-- centroid after GDELT clustering. lists=10 is appropriate
-- for expected sub-theme counts (tens per topic, not
-- thousands) — lower lists = faster build, sufficient recall.
CREATE INDEX idx_sub_themes_centroid ON sub_themes
    USING ivfflat (centroid vector_cosine_ops) WITH (lists = 10);

-- -------------------------------------------------------------
-- 10. SUB THEME MEMBERSHIPS
-- Tracks which articles belong to which sub-theme as of the
-- most recent discovery run for a given topic.
--
-- membership_type:
--   news   — article came from RSS/API sources and formed the
--            cluster via HDBSCAN clustering
--   reddit — post was assigned to this sub-theme by nearest-
--            centroid similarity after clustering completed
--
-- similarity_to_centroid: cosine similarity of the article
-- embedding to the sub-theme centroid vector. The news article
-- with the highest similarity becomes the
-- representative_article_id on the sub_themes row.
--
-- This table represents CURRENT state only — not history.
-- On each discovery run for a topic, all existing membership
-- rows for that topic are deleted and replaced with the new
-- result. Historical volume counts are preserved in
-- sub_theme_snapshots (table 11), not here.
-- -------------------------------------------------------------
CREATE TABLE sub_theme_memberships (
    id                      UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    sub_theme_id            UUID NOT NULL REFERENCES sub_themes (id) ON DELETE CASCADE,
    article_id              UUID NOT NULL REFERENCES articles (id) ON DELETE CASCADE,
    membership_type         TEXT NOT NULL CHECK (membership_type IN ('news', 'reddit')),
    similarity_to_centroid  FLOAT CHECK (similarity_to_centroid BETWEEN -1 AND 1),
    created_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (sub_theme_id, article_id)
);

CREATE INDEX idx_stm_sub_theme_id ON sub_theme_memberships (sub_theme_id);
CREATE INDEX idx_stm_article_id   ON sub_theme_memberships (article_id);

-- -------------------------------------------------------------
-- 11. SUB THEME SNAPSHOTS
-- Time-series table. One row per sub-theme per discovery run.
-- Records the full observable state of a sub-theme at the
-- moment the discovery job completed for this topic.
--
-- This table serves three purposes:
--   1. Evolution detection — current snapshot vs previous
--      snapshot to detect volume changes and sentiment shifts
--   2. Sentiment baseline — rolling SUBTHEME_BASELINE_DAYS
--      average computed from this table's history
--   3. Timeline API — GET /topics/{id}/intelligence/timeline
--      reads this table to build the evolution view
--
-- article_count:     news articles (RSS/API) in the cluster
-- reddit_post_count: Reddit posts assigned by centroid
-- total_volume:        sum of the two above — used for
--                      growth and decline detection
--
-- sentiment_score: VADER compound score aggregated over all
-- Reddit comments for Reddit posts assigned to this sub-theme,
-- weighted by comment upvote score. Range -1.0 to 1.0.
-- NULL when no Reddit posts were assigned or none had comments.
--
-- sentiment_label: derived from sentiment_score at write time:
--   score >= 0.05  → 'positive'
--   score <= -0.05 → 'negative'
--   otherwise      → 'neutral'
--   NULL when sentiment_score is NULL.
-- -------------------------------------------------------------
CREATE TABLE sub_theme_snapshots (
    id                  UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    sub_theme_id        UUID NOT NULL REFERENCES sub_themes (id) ON DELETE CASCADE,
    topic_id            UUID NOT NULL REFERENCES topics (id) ON DELETE CASCADE,
    article_count       INT NOT NULL DEFAULT 0,
    reddit_post_count   INT NOT NULL DEFAULT 0,
    total_volume        INT NOT NULL DEFAULT 0,
    sentiment_score     FLOAT CHECK (sentiment_score BETWEEN -1 AND 1),
    sentiment_label     TEXT CHECK (sentiment_label IN ('positive', 'neutral', 'negative')),
    status              TEXT NOT NULL
                            CHECK (status IN ('emerging', 'active', 'declining', 'inactive')),
    snapshot_at         TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_sts_sub_theme_id          ON sub_theme_snapshots (sub_theme_id);
CREATE INDEX idx_sts_topic_id              ON sub_theme_snapshots (topic_id);
CREATE INDEX idx_sts_snapshot_at           ON sub_theme_snapshots (snapshot_at DESC);

-- Composite index: the primary query pattern is "give me the
-- last N snapshots for this sub-theme ordered by recency."
-- Used by both evolution detection logic and the timeline
-- API endpoint. A single composite index satisfies both the
-- filter and the sort from one index scan.
CREATE INDEX idx_sts_sub_theme_snapshot_at ON sub_theme_snapshots (sub_theme_id, snapshot_at DESC);

-- -------------------------------------------------------------
-- 12. INTELLIGENCE ALERTS
-- Separate from the alerts table — intelligence alerts are
-- triggered by sub-theme state changes, not by individual
-- articles. Mixing them into the existing alerts table would
-- require making article_id nullable and adding complex CHECK
-- constraints to preserve integrity. A separate table keeps
-- both schemas clean and independently queryable.
--
-- alert_type maps directly to the four detectable events:
--   sub_theme_emerging        — new sub-theme detected
--   sub_theme_growing         — volume crossed growth threshold
--   sub_theme_disappearing    — volume dropped below threshold
--   sub_theme_sentiment_shift — sentiment delta vs baseline
--                               exceeded shift threshold
--
-- payload: JSONB snapshot of the sub-theme state at alert
-- time (label, description, volume, sentiment, status).
-- Stored so the alert renders correctly even if the sub-theme
-- continues to evolve before the user reads it.
--
-- Idempotency: the UNIQUE constraint on
-- (user_id, sub_theme_snapshot_id, alert_type, channel)
-- means a replayed sub-theme-events Kafka message produces
-- a duplicate INSERT that is silently ignored via
-- ON CONFLICT DO NOTHING — identical pattern to the alerts
-- table UNIQUE constraint.
-- -------------------------------------------------------------
CREATE TABLE intelligence_alerts (
    id                      UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    user_id                 UUID NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    topic_id                UUID NOT NULL REFERENCES topics (id) ON DELETE CASCADE,
    sub_theme_id            UUID NOT NULL REFERENCES sub_themes (id) ON DELETE CASCADE,
    sub_theme_snapshot_id   UUID NOT NULL REFERENCES sub_theme_snapshots (id) ON DELETE CASCADE,
    alert_type              TEXT NOT NULL
                                CHECK (alert_type IN (
                                    'sub_theme_emerging',
                                    'sub_theme_growing',
                                    'sub_theme_disappearing',
                                    'sub_theme_sentiment_shift'
                                )),
    channel                 TEXT NOT NULL CHECK (channel IN ('email', 'sms', 'websocket')),
    status                  TEXT NOT NULL DEFAULT 'pending'
                                CHECK (status IN ('pending', 'sent', 'failed')),
    payload                 JSONB NOT NULL,
    sent_at                 TIMESTAMPTZ,
    created_at              TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    UNIQUE (user_id, sub_theme_snapshot_id, alert_type, channel)
);

CREATE INDEX idx_ia_user_id_created_at ON intelligence_alerts (user_id, created_at DESC);
CREATE INDEX idx_ia_status             ON intelligence_alerts (status);
CREATE INDEX idx_ia_sub_theme_id       ON intelligence_alerts (sub_theme_id);

